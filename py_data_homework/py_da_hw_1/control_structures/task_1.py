ids = {'user1': [213, 213, 213, 15, 213],
       'user2': [54, 54, 119, 119, 119],
       'user3': [213, 98, 98, 35]}

# Создаем пустой список
all_ = []

# Перебираем значения и добавляем их в список
for i in ids.values():
  all_ += i

# Создаем множество
set_all = set(all_)

# Выводим множество на экран
print(set_all)
